							<td colspan="2"><span class="anchor_t" id="plenary1"></span><b>Plenary 1: Changing trends in work and technology—What it means to you</b><br><br>
								This plenary focuses on providing perspectives from the government, private sector and academia on key developments and trends that might impact the workforce and workplaces in the next 10 years. The panel will focus on possible threats and opportunities these changes may bring to the WSH community and provide WSH insights that will equip businesses, workplaces and workers of the future. 
