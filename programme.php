							<td><span class="anchor_t" id="symposium5"></span><b>Symposium 5: A safe and healthy workplace makes business sense—The key levers</b><br><br>
								This symposium aims to evaluate the complex relationship between work environment and the overall health, safety and wellbeing of employees. While there are many things employers can do to enhance opportunities for the total safety, health and wellbeing of employees, how do they know which ones are the most efficient or cost-effective? In this session, speakers from different countries and WSH background will elaborate on how a safe and healthy workplace makes business sense. Their sharing will illuminate what indicators companies can use to measure the success of their safety and health interventions.
							</td>
							<td><span class="anchor_t" id="symposium6"></span><b>Symposium 6: Communicating WSH in a diverse workplace</b><br><br><br>
								This session looks at how a better appreciation and application of communication could improve WSH performance of workers within a dynamic and diverse workplace. Speakers will share international initiatives, case studies and practical tips on how minor tweaks to one’s daily communication and messaging habits would harness benefits of a diverse workplace and help improve an organisation’s overall performance.
