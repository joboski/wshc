							<td width="50%"><span class="anchor_t" id="symposium1"></span><b>Symposium 1: Be the difference in WSH</b><br><br><br>
								When Apple came up with the slogan, “Think Different”, in 1997, it propelled the company forward and ahead of its competition. Although the advertising slogan was discontinued in 2002, the company’s dominance in the marketplace is still felt today. In the workplace safety and health playing field, what can companies do to “think different”, BE the difference and BE a leader in the industry? This session examines companies that have done just that.
							</td>
							<td width="50%"><span class="anchor_t" id="symposium2"></span><b>Symposium 2: Emerging issues: Occupational diseases and WSH in transportation</b><br><br>
								The WSH landscape has changed over the past decade with new trends, such as a rise in the incidence of occupational diseases and work-related traffic accidents, emerging. This session provides perspectives on these new concerns and approaches to address them.
							</td>
						</tr>
