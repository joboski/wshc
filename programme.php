							<td width="50%"><span class="anchor_t" id="symposium3"></span><b>Symposium 3: Business sustainability: WSH & crisis management</b><br><br>
								It is of utmost importance for a business to be ready for an unexpected crisis so that it can respond quickly to mitigate its impact and minimise business disruption. In this session, we will hear from local and global companies how they prepare and manage crisis in a timely and safe manner. The speakers will also explore WSH implications from upstream preventive measures till recovery of the business.
							</td>
							<td width="50%"><span class="anchor_t" id="symposium4"></span><b>Symposium 4: Total WSH—Your role in the team</b><br><br>
								Total WSH (Total Workplace Safety and Health) is a comprehensive and integrated approach to manage WSH holistically. What are the roles of employers and employees in Total WSH? In this session, the speakers will provide an interactive and engaging experience for participants to appreciate the roles played by all employers and employees within an organisation.
