							<td colspan="2"><span class="anchor_t" id="plenary2"></span><b>Plenary 2: Vision Zero—Inculcating the mindset of being safe and healthy</b><br><br>
								What entails a Vision Zero mindset? Is it an intricate set of connected systems? Even with the best effort and intent, systems may only be as good as the people operating it. To achieve Vision Zero, what more must we do? This plenary session examines who are the stakeholders driving the Vision Zero mindset and the roles they have to play to achieve it.
